package wah06.constant;

public class W6C1 {
	public static void main(String[] args) {
		final double PI = 3.14159;
		
		
		int r = 10;
		
		double area = PI*r*r;
		
		System.out.println(area);
		
		System.out.println(Math.PI);
		
		area = 0;
		
		area = Math.PI * Math.pow(r, 2);
		
		System.out.println(area);
		
	}
}
