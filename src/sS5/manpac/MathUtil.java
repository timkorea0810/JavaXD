package sS5.manpac;

public class MathUtil {
	public double pi = 3.14159;
	
	
	public double getArea(int r) {
		double area = pi*r*r;
		
		return area;
	}
	
	//
	public int sum(int a, int b) {
		int result = a + b;
		
		return result;
	}
	
	//
	public int sum(int a, int b, int c) {
		int result = a + b + c;
		
		return result;
	}
}
